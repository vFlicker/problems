/*
  Написати функцію яка повертає кількість голосних
  у заданому рядку. Ми будемо розглядати a, e, i, o, u
  як голосні (але не y). Рядок введення складатиметься
  лише з малих літер та/або пробілів.
*/

const getCount = (str) => (str.match(/[aeiou]/gi) || []).length;

console.log(getCount('foo bar baz')); // 4
